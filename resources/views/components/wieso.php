    <h2>Wie funktioniert StudyGenie?</h2>
    <div class="wiesoContentContainer">
        <p>Viele kennen LLMs (Large Language Models) wie ChatGPT der Firma OpenAI oder Gemini von Google als vielseitige KI-Tools. Bei StudyGenie haben wir diese Technologie speziell für Bildungs- und Karrierefragen angepasst, um Schülern und Studenten gezielt bei Lern- und Berufsfragen zu helfen.</p>
        <div style="text-align: center;"><strong>Unser Motto: "Hilfe zur Selbsthilfe"</strong></div><br />
         <p>Statt einfach nur Lösungen zu präsentieren, hilft StudyGenie dabei, die eigenen Lösungen zu finden.<br />
         Zudem bietet StudyGenie die einfache Erstellung von Bewerbungsunterlagen nach DIN-Normen, sodass alle Werkzeuge für schulischen und beruflichen Erfolg an einem Ort verfügbar sind.<br />
        </p> 
         <b>Weiterer Vorteil: Alle Daten bleiben von Trainingszwecken ausgeschlossen.</b>
